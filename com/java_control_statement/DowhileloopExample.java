package com.java_control_statement;

public class DowhileloopExample {
public static void main(String[] args) {
	int i=1; 
	do{
		System.out.println(i);
	     i++;
	    }while(i<=10);
	
	String[] cars = {"honda" ,"hero","ninja"};
	int j= 0;
	do {
		System.out.println(cars[j]);
		j++;
		
	}while(j<cars.length);
}
}
